package com.social.pojo;

import java.io.Serializable;
import java.util.Date;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author LENOVO
 */
@Entity
@Table(name = "post_report")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "PostReport.findAll", query = "SELECT p FROM PostReport p"),
    @NamedQuery(name = "PostReport.findById", query = "SELECT p FROM PostReport p WHERE p.id = :id"),
    @NamedQuery(name = "PostReport.findByContent", query = "SELECT p FROM PostReport p WHERE p.content = :content"),
    @NamedQuery(name = "PostReport.findByCreatedDate", query = "SELECT p FROM PostReport p WHERE p.createdDate = :createdDate"),
    @NamedQuery(name = "PostReport.findByModifiedDate", query = "SELECT p FROM PostReport p WHERE p.modifiedDate = :modifiedDate")})
public class PostReport implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @NotNull
    @Column(name = "id")
    private Integer id;
    @Size(max = 255)
    @Column(name = "content")
    private String content;
    @Column(name = "created_date")
    @Temporal(TemporalType.TIMESTAMP)
    private Date createdDate;
    @Column(name = "modified_date")
    @Temporal(TemporalType.TIMESTAMP)
    private Date modifiedDate;
    @JoinColumn(name = "post_id", referencedColumnName = "id")
    @ManyToOne(optional = false)
    private Post postId;
    @JoinColumn(name = "user_report_id", referencedColumnName = "id")
    @ManyToOne(optional = false)
    private User userReportId;

    public PostReport() {
    }

    public PostReport(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Date getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(Date createdDate) {
        this.createdDate = createdDate;
    }

    public Date getModifiedDate() {
        return modifiedDate;
    }

    public void setModifiedDate(Date modifiedDate) {
        this.modifiedDate = modifiedDate;
    }

    public Post getPostId() {
        return postId;
    }

    public void setPostId(Post postId) {
        this.postId = postId;
    }

    public User getUserReportId() {
        return userReportId;
    }

    public void setUserReportId(User userReportId) {
        this.userReportId = userReportId;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof PostReport)) {
            return false;
        }
        PostReport other = (PostReport) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.social.pojo.PostReport[ id=" + id + " ]";
    }
    
}
